class Caculator(object):

    def __init__(self):
        pass

    def add(self, num_a, num_b):
        return num_a + num_b

    def minus(self, num_a, num_b):
        return num_a - num_b

    def multiply(self, num_a, num_b):
        return num_a * num_b

    def devide(self, num_a, num_b):
        return num_a / num_b
